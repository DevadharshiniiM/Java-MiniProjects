public class ifelse {
    public static void main(String [] args)
    {
        int avg = 20;
        if(avg>=75)
        {
            System.out.println("Distinction");
        }
        else if(avg>=60)
        {
            System.out.println("First Class");
        }
        else if(avg>=50)
        {
            System.out.println("Second Class");
        }
        else if (avg>=40)
        {
            System.out.println("Third Class");
        }
        else
        {
            System.out.println("Fail");
        
        }
    }
        }
        
    

